# These operators are used to perform mathematical opeartions

a = 10
b = 3

# 1. Addition
add = a+b
print(add)

# 2. Subtraction
sub = a-b
print(sub)

# 3. Multiplication
mul = a*b
print(mul)

# 4. Division
div = a/b   # gives division answer
print(div)

# 5. Floor division
f_div = a//b   # gives quotient value
print(f_div)

# 6. Modulus
mod = a%b   # gives remainder value
print(mod)

# 7. Exponentiation
exp = a**b   # It is like 10^2
print(exp)