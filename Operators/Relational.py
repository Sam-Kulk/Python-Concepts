'''
The Relational operators are also called as Comparison operators
These operators perform comparison operation between two values/variables
These operators boolean values i.e. True/False as a output/result
'''

a = 5
b = 10

# 1. Equal
print(a==b)

# 2. Not Equal
print(a!=b)

# 3. Greater than
print(a>b)

# 4. Less than
print(a<b)

# 5. Greater than or Equal to
print(a>=b)  # gives True if anyone condition is true, gives False if both conditions are false

# 6. Less than or Equal to
print(a<=b)  # gives True if anyone condition is true, gives False if both conditions are false