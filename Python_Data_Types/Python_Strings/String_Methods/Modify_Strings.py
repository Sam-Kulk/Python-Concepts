# 1. upper()
print(s.upper())  # I cannot do upper("Hi there, hello")

# 2. lower()

# 3. swapcase() - converts the case of the string
print(s.swapcase())

# 4. strip()

# 5. replace() # I can replace substring, which can be even one character as well

# 6. split() # Returns list
print(s.split(","))