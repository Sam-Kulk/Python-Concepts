'''
'+' operator is used for string Concatenation
syntax : str1+str2

When '+' operator is used between two numbers(int or float or boolean or combo), it will do addition operation.
When '+' operator is used between two strings, it will perform concatination operation
When '+' operator is used between two values/variables of different data-types, then error is obtained in run-time
'''


a = 'Samarth'
b = 'Kulkarni'
c = a+b
# or
c = 'Samarth'+'Kulkarni'
print(c)

# To provide space
c = a+" "+b
print(c)